  const appearOptions = { threshold: 0.1, rootMargin: '0px 0px -50px 0px' };
  const appearOnScroll = new window.IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
      if (!entry.isIntersecting) return;
